// Summary Comment - Values can pass into a function with arguments. A return statement
// can be used to send a value back out of a function.

// Example
function minusSeven(num) {
  return num - 7;
}

// Only change code below this line
function timesFive(num) {
  return num * 5;
}
var newValue = timesFive(3);

console.log(newValue);
console.log(minusSeven(10));
