// Summary Comment - Parameters are variables that act as placeholders for the values
// that are to be input to a function when it is called.  The actual values that are
// input (or "passed") into a function when it is called are known as arguments.


// Example
function ourFunctionWithArgs(a, b) {
  console.log(a - b);
}
ourFunctionWithArgs(10, 5); // Outputs 5

